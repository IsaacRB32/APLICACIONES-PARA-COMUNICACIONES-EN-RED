    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        try {
            usuario = txtUsuario.getText().trim();

            if (usuario.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Por favor ingresa un nombre de usuario.");
                return;
            }

            // Crear socket cliente en un puerto aleatorio
            socket = new DatagramSocket();

            // Construir mensaje de inicio
            String inicioXML = "<inicio><usr>" + usuario + "</usr></inicio>";
            byte[] buffer = inicioXML.getBytes();

            // Enviar al servidor (localhost, puerto 8000)
            InetAddress ip = InetAddress.getByName("127.0.0.1");
            DatagramPacket paquete = new DatagramPacket(
                    buffer,
                    buffer.length,
                    ip,
                    8000
            );

            socket.send(paquete);

            System.out.println("Mensaje enviado al servidor: " + inicioXML);

            javax.swing.JOptionPane.showMessageDialog(this,
                    "Conectado al servidor.\nUsuario: " + usuario);

            // Abrir ventana de selecci√≥n de salas
            SalaSelector selector = new SalaSelector(usuario, socket);
            selector.setVisible(true);

            this.dispose();

        }catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error al conectar con el servidor: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

